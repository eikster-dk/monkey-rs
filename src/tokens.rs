#[derive(Debug, Clone, PartialEq)]
pub enum Token {
    Illegal,
    EOF,
    Identifier(String),
    Int(isize),
    Assign,
    Plus,
    Comma,
    Semicolon,
    LParenthesis,
    RParenthesis,
    LBrace,
    RBrace,
    Function,
    Bang,
    LT,
    GT,
    Minus,
    Slash,
    Asterisk,
    Let,
    If,
    Else,
    Boolean(bool),
    Return,
    Equal,
    NotEqual,
}
